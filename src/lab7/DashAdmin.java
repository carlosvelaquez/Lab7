    private void btnpAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnpAñadirActionPerformed
        /**
         * Date fechaIngreso, Date fechaAlta, String tipoSangre, String causaVisita, 
         * String nombre, int edad, int id, float altura, float peso, char sexo
         */
        char sexoChar;
        
        if (rbnpFemenino.isSelected()) {
            sexoChar = 'F';
        }else{
            sexoChar = 'M';
        }
        
        Paciente p;
        p = new Paciente(dcnpIngreso.getDate(), dcnpAlta.getDate(), tfnpTipoSangre.getText(),
                tanpCausaVisita.getText(), tfnpNombre.getText(), (Integer) spnpEdad.getValue(), Integer.parseInt(tfnpID.getText()), (Integer) spnpAltura.getValue(),
                (Integer) spnpPeso.getValue(), sexoChar);
        
        DefaultListModel m = (DefaultListModel) listnpAlergias.getModel();
        for (int i = 0; i < m.getSize(); i++) {
            p.getAlergias().add((String) m.getElementAt(i));
        }
        
        m = (DefaultListModel) listnpOrganos1.getModel();
        for (int i = 0; i < m.getSize(); i++) {
            p.getOrganos().add((Organo) m.getElementAt(i));
        }
        
        Start.pacientes.add(p);
        JOptionPane.showMessageDialog(this, "Paciente añadido exitosamente");
        
        
    }//GEN-LAST:event_btnpAñadirActionPerformed

